# >     more than
# <     less than
# >=    more or equal than
# <=    less or equal than
# ==    equal to
# !=    different from

my_bool = 10 == 10
print(my_bool)
print("-" * 50)
print("")

my_bool = 5 + 5 == 10
print(my_bool)
