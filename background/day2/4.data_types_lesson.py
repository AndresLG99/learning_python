# String (str)          - "hello"                       - text
# Integer (int)         - 1,2,3,4                       - numbers with no decimals points
# Floating (float)      - 1.2,2.3                       - numbers with decimal points
# List (list)           - [1,"a",2,"b"]                 - ordered collection of elements
# Dictionary (dict)     - {"color":"red","number":1}    - pair of key-value, just as a dictionary
# Tuple (tuple)         - ("Monday","Tuesday")          - ordered collection of elements (can't be changed)
# Set (set)             - {"a","b","c"}                 - ordered collection of elements (unique)
# Boolean (bool)        - True,False                    - ranges between True or False only